import fractions

frac1 = fractions.Fraction(1, 2)
frac2 = fractions.Fraction(1, 3)
print(frac1 + frac2)
print(frac1 - frac2)
print(frac1 * frac2)
print(frac1 / frac2)
print(frac1**2)
print(frac2**3)
print(frac1**-1)
print(frac2**-1)
